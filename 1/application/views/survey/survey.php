            <div class="modal-content">
              <div class="modal-header">
                <button type="button" class="close" data-dismiss="modal" aria-hidden="true">&times;</button>
                <h4 class="modal-title" id="myModalLabel">工作与生活（HR）</h4>
              </div>
              <div class="modal-body">
                  <p><b>以互联网招聘工作为例：</b></p>

<p>9点半到公司（一般互联网公司上班弹性，可以晚一点到公司，当然你下班也会晚），路上把早餐吃了，速度越快越好，不然你就迟到了。整理一下当天要做的工作，做个list，然后开始1天的工作。</p>

<p>今天的面试安排往往前一天就安排好了，周一、周二会比较忙，正常招聘水平，每天应该面试8-10人，如果招聘量大，一天能面试10-15人；</p>

<p>招聘是个体力+技术活，一半的工作量是沟通协调安排面试过程，纯事务性工作，另外一半时间在够候选人沟通，面试官沟通，这是个技术活。</p>

<p>跟候选人沟通，需要你短时间内（5-20分钟）初步判断候选人的基本情况，综合素质是否达到职位要求，一般都是用STAR原则，行为面试法快速判断一个人的过往经历，通过快速沟通候选人的简历信息，判断候选人的能力素质，兴趣爱好，性格等情况，这部分主要靠的是面试官的知识阅历和判断力，通过以上2个方面的判断，初步确定候选人是否符合职位要求，如果不符合，结束面试，符合继续安排复试。</p>

<p>跟面试官沟通，首先你要了解面试官的需求和判断标准，倾听他的面试评价，快速判断候选人是否能通过面试，如果面试官犹豫不决时，你需要从你对候选人的判断，去影响面试官，推进面试流程，最后确定人选。</p>

<p>如果确定好人选，就要进入薪资谈判环节，这是个技术活，需要你根据公司内部的薪资标准及面试官的建议，跟候选人谈薪资，往往候选人的薪资期望会高于公司能给的水平，这时候就需要HR，跟候选人沟通谈判，掌握候选人的痛点，从薪资、发展空间、工作环境等候选人在意的地方，说服候选人，完成入职工作。</p>

